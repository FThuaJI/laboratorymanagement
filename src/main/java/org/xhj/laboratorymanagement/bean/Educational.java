package org.xhj.laboratorymanagement.bean;

public class Educational {

    private Integer educational_id;
    private String educational_name;
    private String educational_num;
    private String educational_pwd;

    public Educational() {
    }

    public Integer getEducational_id() {
        return educational_id;
    }

    public void setEducational_id(Integer educational_id) {
        this.educational_id = educational_id;
    }

    public String getEducational_name() {
        return educational_name;
    }

    public void setEducational_name(String educational_name) {
        this.educational_name = educational_name;
    }

    public String getEducational_num() {
        return educational_num;
    }

    public void setEducational_num(String educational_num) {
        this.educational_num = educational_num;
    }

    public String getEducational_pwd() {
        return educational_pwd;
    }

    public void setEducational_pwd(String educational_pwd) {
        this.educational_pwd = educational_pwd;
    }
}
